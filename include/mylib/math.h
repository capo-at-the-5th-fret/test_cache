#pragma once

namespace mylib
{
    inline int add(int a, int b)
    {
        return a + b;
    }

    inline int subtract(int a, int b)
    {
        return a - b;
    }

    inline int multiply(int a, int b)
    {
        return a * b;
    }

    inline int divide(int a, int b)
    {
        return a / b;
    }
}
